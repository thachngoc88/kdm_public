    {
        //delete all data.
        DB::statement('ALTER TABLE questions AUTO_INCREMENT = 1;');

        $workbooks = \App\Workbook::all();

        foreach ($workbooks as $wb) {
            for ($i = 0; $i < 10; $i++) {
                $question = Question::create([
                    "workbook_id" => $wb->id,
                    "text" => $this->questionsHash[$wb->id][$i],
                ]);
                $question->save();
            }
        }
    }
